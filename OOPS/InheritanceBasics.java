// Base/Parent Class
class Animal{
    String color;
    void eat(){
        System.out.println("Eats");
    }
    void breathe(){
        System.out.println("Breathes");
    }
}

// type1- Derived/Child Class
class Mammal extends Animal{
    int legs;
    void walk(){
        System.out.println("Walks");
    }
}

